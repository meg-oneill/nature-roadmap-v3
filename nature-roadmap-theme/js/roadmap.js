function toggleActions() {
  const grid = document.getElementById('irmGrid');
  const btn = document.querySelector('.irm-h-2028-hint');
  const isExpanded = grid.classList.toggle('irm-expanded');
  if (btn) {
    btn.innerHTML = '<span class="irm-h-2028-arrow">' + (isExpanded ? '&#x25C0;' : '&#x25B6;') + '</span> ' + (isExpanded ? 'Hide actions' : 'Show actions');
  }
}

// On page load, briefly expand the grid invisibly to capture row heights
window.addEventListener('DOMContentLoaded', function() {
  const grid = document.getElementById('irmGrid');
  if (!grid) return;
  // Disable transitions so the expand is instant and invisible
  grid.style.transition = 'none';
  grid.classList.add('irm-expanded');
  // Force layout recalc
  void grid.offsetHeight;
  // Capture the row heights
  var rows = getComputedStyle(grid).gridTemplateRows;
  // Collapse back
  grid.classList.remove('irm-expanded');
  void grid.offsetHeight;
  // Lock the row heights
  grid.style.gridTemplateRows = rows;
  // Re-enable transitions on next frame
  requestAnimationFrame(function() {
    grid.style.transition = '';
  });
});

// Panel functions
function openPanel(id) {
  const data = panelData[id];
  if (!data) return;

  document.getElementById('panelPrinciple').textContent = data.principle;
  document.getElementById('panelTitle').textContent = data.title;
  document.getElementById('panelTime').textContent = data.time;

  let html = '';

  // Context
  html += '<div class="irm-panel-context">' + data.context + '</div>';

  // Actions needed
  html += '<div class="irm-panel-section actions-section">';
  html += '<h4>Actions Needed in the Next Five Years</h4>';
  html += '<ul>';
  data.actions.forEach(a => { html += '<li>' + a + '</li>'; });
  html += '</ul></div>';

  // GBCA actions
  html += '<div class="irm-panel-section gbca-section">';
  html += '<h4>What GBCA Is Doing</h4>';
  html += '<ul>';
  data.gbca.forEach(g => { html += '<li>' + g + '</li>'; });
  html += '</ul></div>';

  // Industry actions
  html += '<div class="irm-panel-section industry-section">';
  html += '<h4>What Industry Needs to Do</h4>';
  html += '<ul>';
  data.industry.forEach(i => { html += '<li>' + i + '</li>'; });
  html += '</ul></div>';

  document.getElementById('panelBody').innerHTML = html;
  document.getElementById('irmOverlay').classList.add('open');
  document.getElementById('irmPanel').classList.add('open');
  document.body.style.overflow = 'hidden';
}

function closePanel() {
  document.getElementById('irmOverlay').classList.remove('open');
  document.getElementById('irmPanel').classList.remove('open');
  document.body.style.overflow = '';
}

// Close panel with Escape key
document.addEventListener('keydown', (e) => {
  if (e.key === 'Escape') closePanel();
});

// Staggered animation for roadmap grid cells
const irmObserver = new IntersectionObserver((entries) => {
  entries.forEach(entry => {
    if (entry.isIntersecting) {
      const targets = entry.target.querySelectorAll('.irm-target');
      targets.forEach((t, i) => {
        t.style.animationDelay = (i * 0.04) + 's';
      });
      irmObserver.unobserve(entry.target);
    }
  });
}, { threshold: 0.1 });

const irmGrid = document.getElementById('irmGrid');
if (irmGrid) irmObserver.observe(irmGrid);

